    private void tblCust_showMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCust_showMouseClicked
        
         if (tblCust_show.getSelectedRow()>-1){
             
            int r=tblCust_show.getSelectedRow();
            
            txtCust_no.setText(String.valueOf(tblCust_show.getValueAt(r, 3)));
            txtCust_name.setText(String.valueOf(tblCust_show.getValueAt(r, 2)));
            txtCust_add.setText(String.valueOf(tblCust_show.getValueAt(r, 1)));
            txtCust_tel.setText(String.valueOf(tblCust_show.getValueAt(r, 0)));
             
            btnState(1, 0, 1, 1);
         }
    }//GEN-LAST:event_tblCust_showMouseClicked

