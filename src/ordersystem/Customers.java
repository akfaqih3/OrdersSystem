    private void btnCust_savActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCust_savActionPerformed
        if (!(txtCust_name.getText().isBlank())){
            String cust_name,cust_add,cust_tel,sql;
            
            cust_name=txtCust_name.getText();
            cust_add=txtCust_add.getText();
            cust_tel=txtCust_tel.getText();
           
            sql="insert into customers(cust_name,cust_add,cust_tel) "
                    + "values('"+cust_name+"','"+cust_add+"','"+cust_tel+"');";
            
            if (DB.Calldb.saveData(sql)){
                JOptionPane.showMessageDialog(null, "تم الحفظ بنجاح.");
                
                txtState();
                
                btnState(0, 1, 0, 0);
                
            }
            else{
                JOptionPane.showMessageDialog(null, "لم تتم العملية.");
               
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "يرجى ملئ الحقول المطلوبة.");
        }
        refreshTable(tblCust_show);
    }//GEN-LAST:event_btnCust_savActionPerformed

    private void btnCust_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCust_delActionPerformed
         if (!txtCust_no.getText().isBlank()){
        int cust_no;
        String sql;
        cust_no=Integer.valueOf(txtCust_no.getText());
        sql="delete from customers where cust_no="+cust_no+";";
        
         if (DB.Calldb.deleteData(sql)){
            JOptionPane.showMessageDialog(null, "تم الحذف بنجاح.");
            
            txtState();
            
            btnState(0,1,0,0);
        }
        else{
            JOptionPane.showMessageDialog(null, "لم تتم العملية.");
        }
        }
        else{
            JOptionPane.showMessageDialog(null, "يرجى ملئ حقل الرقم.");
        }
         refreshTable(tblCust_show);
    }//GEN-LAST:event_btnCust_delActionPerformed

    private void btnCust_edtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCust_edtActionPerformed
        
        if (!(txtCust_name.getText().isBlank())){
            String cust_name,cust_add,cust_tel,sql;
            int cust_no;
            
            cust_no=Integer.valueOf(txtCust_no.getText());
            cust_name=txtCust_name.getText();
            cust_add=txtCust_add.getText();
            cust_tel=txtCust_tel.getText();
            sql="update customers set cust_name='"+cust_name+"',"
                    + "cust_add='"+cust_add+"',"
                    + "cust_tel='"+cust_tel+"'where cust_no='"+cust_no+"';";
            
            
            if (DB.Calldb.updateData(sql)){
                JOptionPane.showMessageDialog(null, "تم التعديل بنجاح.");
                
                txtState();
                btnState(0, 1, 0, 0);
            }
            else{
                JOptionPane.showMessageDialog(null, "لم تتم العملية.");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "يرجى ملئ كل الحقول.");
            
        }
        refreshTable(tblCust_show);
        
    }//GEN-LAST:event_btnCust_edtActionPerformed

